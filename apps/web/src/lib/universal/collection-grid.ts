export const scoCollectionEditionMap = new Map<string, ValidEdition>(
  gridCollections.map((a) => [a.collectionId, a.edition]),
);

export function getScoEdition(collectionId: string) {
  const edition = scoCollectionEditionMap.get(collectionId);
  return edition ?? null;
}

export function getEdition(collectionNo: string) {
  const collection = parseInt(collectionNo);

  if (collection >= 101 && collection <= 108) {
    return 1;
  }
  if (collection >= 109 && collection <= 116) {
    return 2;
  }
  if (collection >= 117 && collection <= 120) {
    return 3;
  }
  return null;
}

export const editionCollectionNo = new Map<number, number[]>([
  [1, [101, 102, 103, 104, 105, 106, 107, 108]],
  [2, [109, 110, 111, 112, 113, 114, 115, 116]],
  [3, [117, 118, 119, 120]],
]);

export function getFcoCollectionIds(collectionId: string) {
  const edition = getScoEdition(collectionId);
  if (!edition) return [];

  const collectionNo = editionCollectionNo.get(edition);
  if (!collectionNo) return [];

  // todo: should hard coded collection id in future?
  return collectionNo.flatMap((n) => {
    const base = `${collectionId.substring(0, collectionId.length - 4)}${n}`;
    return [`${base}z`, `${base}a`];
  });
}

export function getCollectionEdition(objekt: ValidObjekt) {
  if (objekt.artist === "idntt") {
    return null;
  }
  const edition =
    objekt.class === "Special"
      ? getScoEdition(objekt.slug)
      : objekt.class === "First"
        ? getEdition(objekt.collectionNo)
        : null;
  return edition;
}
